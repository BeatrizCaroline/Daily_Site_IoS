/* Converte código de condição WeatherAPI → emoji */
function conditionEmoji(code, isDay) {
  if (code === 1000) return isDay ? '☀️' : '🌙';
  if ([1003].includes(code)) return isDay ? '⛅' : '🌤️';
  if ([1006,1009].includes(code)) return '☁️';
  if ([1030,1135,1147].includes(code)) return '🌫️';
  if ([1063,1072,1150,1153,1168,1171,1180,1183,1186,1189,1192,1195,1198,1201].includes(code)) return '🌧️';
  if ([1066,1069,1114,1117,1204,1207,1210,1213,1216,1219,1222,1225,1237,1249,1252,1255,1258,1261,1264].includes(code)) return '❄️';
  if ([1087,1273,1276,1279,1282].includes(code)) return '⛈️';
  if ([1240,1243,1246].includes(code)) return '🌦️';
  return '🌡️';
}

/* Dica baseada na condição */
function weatherTip(data) {
  const lang = currentLang;
  const code = data.current.condition.code;
  const isRain = [1063,1150,1153,1180,1183,1186,1189,1192,1195,1240,1243,1246].includes(code);
  const isStorm = [1087,1273,1276,1279,1282].includes(code);
  const isHot = data.current.temp_c >= 30;
  const isUVHigh = data.current.uv >= 6;
  const isCold = data.current.temp_c <= 12;

  const tips = {
    pt: isStorm ? 'Tempestade prevista — evite sair. Se precisar, fique em local seguro.' :
        isRain  ? 'Leve um guarda-chuva — há chances de chuva hoje.' :
        isHot   ? 'Dia quente! Hidrate-se bem e use protetor solar.' :
        isUVHigh? 'Índice UV alto — use protetor solar ao sair.' :
        isCold  ? 'Faz frio hoje — lembre-se de um casaco.' :
                  'Dia agradável! Ótimo para atividades ao ar livre.',
    es: isStorm ? 'Tormenta prevista — evita salir. Si debes hacerlo, busca un lugar seguro.' :
        isRain  ? 'Lleva un paraguas — hay posibilidad de lluvia hoy.' :
        isHot   ? '¡Día caluroso! Mantente hidratado y usa protector solar.' :
        isUVHigh? 'Índice UV alto — usa protector solar al salir.' :
        isCold  ? 'Hace frío hoy — recuerda llevar un abrigo.' :
                  '¡Día agradable! Perfecto para actividades al aire libre.',
    en: isStorm ? 'Storm expected — avoid going out. Stay in a safe place if needed.' :
        isRain  ? 'Bring an umbrella — there is a chance of rain today.' :
        isHot   ? 'Hot day! Stay hydrated and wear sunscreen.' :
        isUVHigh? 'High UV index — wear sunscreen before going out.' :
        isCold  ? 'Cold day — remember to wear a jacket.' :
                  'Pleasant day! Great for outdoor activities.',
  };
  return tips[lang] || tips.pt;
}

/* Renderiza os dados na tela */
function renderWeather(data) {
  const cur  = data.current;
  const loc  = data.location;
  const fore = data.forecast.forecastday;
  const isDay = cur.is_day === 1;

  elIconBig.textContent  = conditionEmoji(cur.condition.code, isDay);
  elTemp.textContent     = `${Math.round(cur.temp_c)}°C`;
  elDesc.textContent     = cur.condition.text;
  elCityName.textContent = `${loc.name}, ${loc.country}`;
  elHumidity.textContent = `${cur.humidity}%`;
  elWind.textContent     = `${Math.round(cur.wind_kph)} km/h`;
  elFeelsLike.textContent= `${Math.round(cur.feelslike_c)}°C`;
  elUV.textContent       = `${cur.uv}`;
  elTipText.textContent  = weatherTip(data);

  // Previsão 7 dias (usa dias disponíveis, máx 7)
  elWeek.innerHTML = '';
  const today = new Date().getDay();
  const dayNames = i18n[currentLang].day_names;

  fore.forEach((day, idx) => {
    const d       = document.createElement('div');
    const date    = new Date(day.date + 'T12:00:00');
    const dayIdx  = date.getDay();
    const isToday = idx === 0;
    const emoji   = conditionEmoji(day.day.condition.code, true);
    const hi      = Math.round(day.day.maxtemp_c);
    const lo      = Math.round(day.day.mintemp_c);

    d.className = 'weather-day' + (isToday ? ' weather-day--today' : '');
    d.innerHTML = `
      <span class="weather-day__name">${isToday ? i18n[currentLang].day_today : dayNames[dayIdx]}</span>
      <span class="weather-day__icon">${emoji}</span>
      <span class="weather-day__temp">${hi}° / ${lo}°</span>
    `;
    elWeek.appendChild(d);
  });
}

/* Mostra estado de loading */
function showLoading() {
  elLoading.style.display = 'flex';
  elError.style.display   = 'none';
  elData.style.display    = 'none';
}

/* Mostra estado de erro */
function showError(msg) {
  elLoading.style.display = 'none';
  elError.style.display   = 'flex';
  elErrorMsg.textContent  = msg || t('weather_error');
  elData.style.display    = 'none';
}

/* Mostra dados */
function showData() {
  elLoading.style.display = 'none';
  elError.style.display   = 'none';
  elData.style.display    = 'block';
}

/* Busca clima por nome de cidade ou coordenadas */
async function fetchWeather(query) {
  showLoading();
  try {
    const url = `https://api.weatherapi.com/v1/forecast.json?key=${API_KEY}&q=${encodeURIComponent(query)}&days=7&aqi=no&alerts=no&lang=${currentLang === 'pt' ? 'pt' : currentLang === 'es' ? 'es' : 'en'}`;
    const res  = await fetch(url);
    if (!res.ok) throw new Error('API error ' + res.status);
    const data = await res.json();
    renderWeather(data);
    showData();
    localStorage.setItem('daily-weather-city', query);
  } catch (err) {
    showError(t('weather_error'));
  }
}

/* Busca sugestões de cidades */
let suggestTimeout = null;
async function fetchSuggestions(query) {
  if (query.length < 2) { closeSuggestions(); return; }
  try {
    const url  = `https://api.weatherapi.com/v1/search.json?key=${API_KEY}&q=${encodeURIComponent(query)}`;
    const res  = await fetch(url);
    if (!res.ok) return;
    const list = await res.json();
    renderSuggestions(list);
  } catch { closeSuggestions(); }
}

function renderSuggestions(list) {
  suggestionsList.innerHTML = '';
  if (!list || list.length === 0) {
    const li = document.createElement('li');
    li.className = 'weather-suggestions__item weather-suggestions__item--empty';
    li.textContent = t('weather_no_results');
    suggestionsList.appendChild(li);
  } else {
    list.forEach(city => {
      const li = document.createElement('li');
      li.className = 'weather-suggestions__item';
      li.innerHTML = `
        <svg xmlns="http://www.w3.org/2000/svg" width="13" height="13" fill="none" viewBox="0 0 24 24" stroke="currentColor" stroke-width="2"><path stroke-linecap="round" stroke-linejoin="round" d="M17.657 16.657L13.414 20.9a1.998 1.998 0 01-2.827 0l-4.244-4.243a8 8 0 1111.314 0z"/><circle cx="12" cy="11" r="3"/></svg>
        ${city.name}, ${city.region ? city.region + ', ' : ''}${city.country}
      `;
      li.addEventListener('click', () => {
        searchInput.value = city.name;
        searchClear.style.display = 'inline-flex';
        closeSuggestions();
        fetchWeather(city.name);
      });
      suggestionsList.appendChild(li);
    });
  }
  suggestionsList.classList.add('is-open');
}

function closeSuggestions() {
  suggestionsList.classList.remove('is-open');
  suggestionsList.innerHTML = '';
}

/* Eventos do campo de busca */
searchInput.addEventListener('input', () => {
  const v = searchInput.value.trim();
  searchClear.style.display = v ? 'inline-flex' : 'none';
  clearTimeout(suggestTimeout);
  suggestTimeout = setTimeout(() => fetchSuggestions(v), 300);
});

searchInput.addEventListener('keydown', e => {
  if (e.key === 'Enter') {
    const v = searchInput.value.trim();
    if (v) { closeSuggestions(); fetchWeather(v); }
  }
  if (e.key === 'Escape') closeSuggestions();
});

searchClear.addEventListener('click', () => {
  searchInput.value = '';
  searchClear.style.display = 'none';
  searchInput.focus();
  closeSuggestions();
});

// Fecha sugestões ao clicar fora
document.addEventListener('click', e => {
  if (!e.target.closest('#weatherSearch') && !e.target.closest('#weatherSuggestions')) {
    closeSuggestions();
  }
});

/* ══════════════════════════════════════
   GEOLOCALIZAÇÃO AUTOMÁTICA
══════════════════════════════════════ */
function loadWeatherByLocation() {
  // 1. Tenta cidade salva
  const saved = localStorage.getItem('daily-weather-city');
  if (saved) { fetchWeather(saved); return; }

  // 2. Tenta geolocalização do browser
  if (navigator.geolocation) {
    navigator.geolocation.getCurrentPosition(
      pos => fetchWeather(`${pos.coords.latitude},${pos.coords.longitude}`),
      ()  => fetchWeather('São Paulo') // fallback
    );
  } else {
    fetchWeather('São Paulo'); // fallback sem suporte
  }
}

